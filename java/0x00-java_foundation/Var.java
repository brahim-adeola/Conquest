/**
 * Var - declare and print variabkes of all primitive types
 *
 */

class Var
{
    public void data_types()
    {
        int number = 37;
        double height = 6.0;
        char gender = 'M';
        boolean inClass = true;
        float percentage = 78.9f;

        System.out.println("int: " + number);
        System.out.println("double: " + height);
        System.out.println("char: " + gender);
        System.out.println("boolean: " + inClass);
        System.out.println("percentage: " + percentage);
    }
}
